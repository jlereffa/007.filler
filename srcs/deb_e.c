#include <filler.h>

void	deb_e(char *s)
{
	write(2, "\033[1;34mDEBUG : ", ft_strlen("\033[1;34mDEBUG : "));
	while (*s)
	{
		write(2, s, 1);
		s++;
	}
	write(2, "\n\033[0m", ft_strlen("\n\033[0m"));
}
