#include <filler.h>

void	deb_i(char *file, int line)
{
	ft_putstr_fd("\033[1;31mFILE : {\033[1;33m", 2);
	ft_putstr_fd(file, 2);
	ft_putstr_fd("\033[1;31m} | LINE : {\033[1;33m", 2);
	ft_putnbr_fd(line, 2);
	ft_putstr_fd("\033[1;31m}\n\033[0m", 2);
}
